function cleanLyrics(text){
  const lines = String(text || '')
    .replace(/\*\*\[[^\]]+\]\*\*/g, '')
    .replace(/^\s*\[[^\]]+\]\s*$/gm, '')
    .replace(/\*\*/g, '')
    .split(/\n/)
    .map(line => line.trim());
  const out = [];
  for (const line of lines){
    if (!line){
      if (out.length && out[out.length - 1] !== '') out.push('');
    } else {
      out.push(line);
    }
  }
  return out.join('\n').replace(/\n{3,}/g, '\n\n').trim();
}

